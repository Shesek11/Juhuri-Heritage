-- Link recipes to tags
INSERT INTO recipe_tag_map (recipe_id, tag_id)
SELECT r.id, t.id
FROM recipes r
CROSS JOIN recipe_tags t
WHERE
    (r.title LIKE '%דושפרה%' AND t.name IN ('shabbat', 'traditional', 'meat'))
    OR (r.title LIKE '%פלוב אוזבקי%' AND t.name IN ('holiday', 'festive', 'traditional', 'meat'))
    OR (r.title LIKE '%קוטב%' AND t.name IN ('shabbat', 'traditional', 'easy'))
    OR (r.title LIKE '%שאח פלוב%' AND t.name IN ('holiday', 'festive', 'fish'))
    OR (r.title LIKE '%פחלווה%' AND t.name IN ('holiday', 'dessert', 'traditional'));

